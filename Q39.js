"use strict";
function city_country1(city, country) {
    return `${city}, ${country}`;
}
const location1 = city_country1("Lahore", "Pakistan");
const location2 = city_country1("New York", "USA");
const location3 = city_country1("Sydney", "Australia");
console.log(location1);
console.log(location2);
console.log(location3);

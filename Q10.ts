// Program 2: Checking if a Number is Even or Odd

let number: number = 7; // Number to be checked

if (number % 2 === 0) {
    console.log(number + " is even."); // Number is even if the remainder of division by 2 is 0
} else {
    console.log(number + " is odd."); // Number is odd if the remainder of division by 2 is not 0
}
"use strict";
// Program 1: Adding Two Numbers
let num1 = 5; // First number to be added
let num2 = 3; // Second number to be added
let sum = num1 + num2; // Adding the two numbers
console.log("The sum of " + num1 + " and " + num2 + " is " + sum);
// Program 2: Checking if a Number is Even or Odd
let number = 7; // Number to be checked
if (number % 2 === 0) {
    console.log(number + " is even."); // Number is even if the remainder of division by 2 is 0
}
else {
    console.log(number + " is odd."); // Number is odd if the remainder of division by 2 is not 0
}
